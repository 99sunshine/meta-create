function getValue(dict: Dictionary, key: string): string | undefined {
  const parts = key.split('.')
  let current: string | Dictionary | undefined = dict
  for (const part of parts) {
    if (typeof current !== 'object' || !current || !(part in current)) return undefined
    current = current[part] as string | Dictionary
  }
  return typeof current === 'string' ? current : undefined
}

export function isAppLocale(value: string | null | undefined): value is AppLocale {
  return value === 'en' || value === 'zh'
}

export function t(locale: AppLocale, key: string, vars?: Record<string, string | number>): string {
  const base = getValue(translations[locale], key) ?? getValue(translations.en, key) ?? key
  if (!vars) return base
  return base.replace(/\{(\w+)\}/g, (_m, name: string) => String(vars[name] ?? ''))
}
